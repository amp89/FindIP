package data;

public class IpSearchObject {
	
	int first;
	int second;
	int third;
	int fourth;
	String accessToken;
	public int getFirst() {
		return first;
	}
	public void setFirst(int first) {
		this.first = first;
	}
	public int getSecond() {
		return second;
	}
	public void setSecond(int second) {
		this.second = second;
	}
	public int getThird() {
		return third;
	}
	public void setThird(int third) {
		this.third = third;
	}
	public int getFourth() {
		return fourth;
	}
	public void setFourth(int fourth) {
		this.fourth = fourth;
	}
	public String getAccessToken() {
		return accessToken;
	}
	public void setAccessToken(String accessToken) {
		this.accessToken = accessToken;
	}
	public IpSearchObject(int first, int second, int third, int fourth, String accessToken) {
		super();
		this.first = first;
		this.second = second;
		this.third = third;
		this.fourth = fourth;
		this.accessToken = accessToken;
	}
	public IpSearchObject() {
		super();
	}
	@Override
	public String toString() {
		return "IpSearchObject [first=" + first + ", second=" + second + ", third=" + third + ", fourth=" + fourth
				+ ", accessToken=" + accessToken + "]";
	}
	
	

}
